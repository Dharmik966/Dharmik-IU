// Define a public class - Java programs are always part of a class

public class HelloBasics {


    // Entry point: this is the method the Java program runs first

    public static void main(String[] args) {


        // Integer → whole number

        int age = 23;

        // Float → number with decimal point (in Java use 'float' or 'double')

        double temperature = 30.5; // 'double' is more precise than 'float'


        // String → text, always wrapped in double quotes

        String name = "Dharmik";


        // Boolean → true or false value

        boolean isStudent = true;


        // Print values to the terminal using System.out.println

        System.out.println("Name: " + name);           // Output: Name: Alice

        System.out.println("Temperature: " + temperature); // Output: Temperature: 36.6


        System.out.println("Age: " + age);             // Output: Age: 25

        System.out.println("Student status: " + isStudent); // Output: true

    }

}